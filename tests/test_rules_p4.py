from src.games.game_p4.rules_p4 import *
from pytest import *


def test_is_over():
    assert is_over([[' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [0, ' ', ' ', 1]],0,0) == (False,0)
    assert is_over([[' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [0, 0, 0, 0]],0,0) == (True,0)
    assert is_over([[' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [1, 1, 1, 1]],0,1) == (True,1)
    assert is_over([[0, ' ', ' ', ' '], [0, ' ', ' ', ' '], [0, ' ', ' ', ' '], [0, ' ', ' ', 1]],0,0) == (True,0)

def test_make_a_move():
    assert make_a_move([[' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [0, ' ', ' ', 1]], 1, 0) ==[[' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [1, ' ', ' ', ' '], [0, ' ', ' ', 1]]
    assert make_a_move([[' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [1, ' ', 0, 0]],0,2) == [[' ', ' ', ' ', ' '], [' ', ' ', ' ', ' '], [' ', ' ', 0, ' '], [1,' ',0,0]]
